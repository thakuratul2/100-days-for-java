class Comparison {
    public static void main(String[] args) {
        // Comparison Operators
        int a = 10;
        int b = 20;
        
        //equal to
        System.out.println("a == b : " + (a == b));
        //not equal to
        System.out.println("a != b : " + (a != b));
        //greater than
        System.out.println("a > b : " + (a > b));
        //less than
        System.out.println("a < b : " + (a < b));
        //greater than or equal to
        System.out.println("a >= b : " + (a >= b));
        //less than or equal to
        System.out.println("a <= b : " + (a <= b));
    }
    
}
